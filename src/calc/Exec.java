package calc;

//import javax.swing.JOptionPane;
public class Exec 
{

	public double add(double x,double y)
	{
		
		double sum = x + y;
		return sum;
		
	}
	
	public double sub(double x,double y)
	{
		
		double diff = x - y;
		return diff;
		
	}
	
	public double prod(double x,double y)
	{
		
		double prod = x * y;
		return prod;
		
	}
	
	public double div(double x,double y)
	{
		
		double div = x / y;
		return div;
		
	}
	
	
	
	
}
